#ifndef LINX_TCP_LISTENER_H
#define LINX_TCP_LISTENER_H

/****************************************************************************************
**  Includes
****************************************************************************************/		
#include "../device/LinxDevice.h"

/****************************************************************************************
**  Type Defs
****************************************************************************************/		
enum LinxTcpListenerState
{
	START, 
	LISTENING, 
	AVAILABLE, 
	ACCEPT, 
	CONNECTED, 
	CLOSE, 
	EXIT
};

/****************************************************************************************
**  Classes
****************************************************************************************/		
class LinxTcpListener
{
	public:
		/****************************************************************************************
		**  Variables
		****************************************************************************************/		
		LinxTcpListenerState State;
		
		/****************************************************************************************
		**  Constructors
		****************************************************************************************/
		LinxTcpListener();		//Default Constructor
		
		/****************************************************************************************
		**  Functions
		****************************************************************************************/		
		virtual int Start();			//Start TCP Stack
		virtual int Listen();			//Listen For Connection
		virtual int Available();		//New Client Connection Available
		virtual int Accept();		//Accept New Client Connection
		virtual int Connected();	//Connected To Client
		virtual int Close();			//Close Client Connection
		virtual int Exit();			//Stop Listening, Close And Exit
		
	private:
		/****************************************************************************************
		**  Variables
		****************************************************************************************/		
				
		/****************************************************************************************
		**  Functions
		****************************************************************************************/
	
};

#endif //LINX_TCP_LISTENER_H