/****************************************************************************************
**  Type Defs
****************************************************************************************/		
enum LinxTcpListenerState
{
	START, 
	LISTENING, 
	AVAILABLE, 
	ACCEPT, 
	CONNECTED, 
	CLOSE, 
	EXIT
};

/****************************************************************************************
**  Classes
****************************************************************************************/		
class LinxTcpListener
{
	public:
		/****************************************************************************************
		**  Variables
		****************************************************************************************/		
		LinxTcpListenerState State;
		
		/****************************************************************************************
		**  Constructors
		****************************************************************************************/
		LinxTcpListener();		//Default Constructor
		
		/****************************************************************************************
		**  Functions
		****************************************************************************************/		
		virtual int Start();
		virtual int Listen();
		virtual int Available();
		virtual int Accept();
		virtual int Connected();
		virtual int Close();
		virtual int Exit();
		
	private:
		/****************************************************************************************
		**  Variables
		****************************************************************************************/		
				
		/****************************************************************************************
		**  Functions
		****************************************************************************************/
	
};

